    private void ativarControleDeAcesso() {
        ControleAcessoDao cDao = new ControleAcessoDao();
        Usuario user = (Usuario) HashSistema.hash.get("usuarioLogado");
        if (!user.getNome().equals("admin")) {
            ControleAcesso controle = cDao.obterLista(" usuario_id='" + user.getId() + "'").get(0);
            if (controle != null) {
                habilitarMenuSuperior(controle);
                habilitarMenuPrincipal(controle);
                jMenuSistema.setEnabled(false);
                jMenuLog.setEnabled(false);
            }
        }
    }

