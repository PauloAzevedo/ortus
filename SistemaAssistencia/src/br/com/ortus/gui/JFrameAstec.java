    private void fechar() {
        int opcao = JOptionPane.showConfirmDialog(rootPane,
                "Deseja mesmo fechar o programa?", "Confirmação",
                JOptionPane.YES_NO_OPTION);
        if (opcao != JOptionPane.YES_OPTION) {
            return;
        } else {
            Usuario user = (Usuario) HashSistema.hash.get("usuarioLogado");
            try {
                TransactionManager.beginTransaction();
                String ip = pegarIPDaMaquina();
                new LogDao().persistir(new Log(user, Utilidades.retData(), "Desktop", "Saida", ip));
                TransactionManager.commit();
            } catch (Exception e) {
                System.out.println(e);
            } finally {
                HashSistema.hash.remove("usuarioLogado");
                System.exit(0);
            }
        }
    }

    private void logoff() {
        int opcao = JOptionPane.showConfirmDialog(rootPane,
                "Deseja mesmo sair?", "Confirmação",
                JOptionPane.YES_NO_OPTION);
        if (opcao != JOptionPane.YES_OPTION) {
            return;
        }
        Usuario user = (Usuario) HashSistema.hash.get("usuarioLogado");
        try {
            TransactionManager.beginTransaction();
            String ip = pegarIPDaMaquina();
            new LogDao().persistir(new Log(user, Utilidades.retData(), "Desktop", "Saida", ip));
            TransactionManager.commit();
        } catch (Exception e) {
            System.out.println(e);
        } finally {
            HashSistema.hash.remove("usuarioLogado");
            dispose();
            JDialog jd = new JDialogLogin(this, true);
            jd.setLocationRelativeTo(null);
            jd.setVisible(true);
        }
    }

