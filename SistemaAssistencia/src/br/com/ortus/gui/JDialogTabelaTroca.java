    private void salvar() {
        if (!validarCampos()) {
            return;
        }
        int opcao = JOptionPane.showConfirmDialog(rootPane,
                "Deseja salvar?", "Confirmação",
                JOptionPane.YES_NO_OPTION);
        if (opcao != JOptionPane.YES_OPTION) {
            return;
        }
        TabelaTroca troca = new TabelaTroca();
        if (!jTextFieldID.getText().isEmpty()) {
            troca.setId(Integer.parseInt(jTextFieldID.getText()));
        }
        troca.setCliente(jTextFieldCliente.getText());
        troca.setQtd(jTextFieldQtd.getText());
        troca.setEquipamento(jComboBoxEquipamento.getSelectedItem().toString());
        troca.setDataPedido(jFormattedTextFieldDataPedido.getText());
        troca.setDataMaxEnvio(jFormattedTextFieldDataMaxEnvio.getText());
        troca.setObs(jTextFieldObs.getText());
        troca.setDevolucao(jComboBoxDevolucao.getSelectedItem().toString());
        troca.setStatusProducao(jComboBoxStatusProducao.getSelectedItem().toString());
        troca.setDataProducao(jFormattedTextFieldDataProducao.getText());

        Usuario user = (Usuario) HashSistema.hash.get("usuarioLogado");
        String ip = pegarIPDaMaquina();
        Log log = new Log(user, Utilidades.retData(), "Desktop", "Inserto/Update", ip);
        
        if (troca.getMomentoAuteracao() == null) {
            List<Log> logs = new ArrayList<Log>();
            logs.add(log);
            troca.setMomentoAuteracao(logs);
        } else {
            troca.getMomentoAuteracao().add(log);
            troca.setMomentoAuteracao(troca.getMomentoAuteracao());
        }


        TransactionManager.beginTransaction();
        try {
            TabelaTrocaDao tDao = new TabelaTrocaDao();
            new LogDao().persistir(log);
            tDao.persistir(troca);

            TransactionManager.commit();

            JOptionPane.showMessageDialog(null, "Troca foi gravada!");
            povoarTabelaPendente();
            povoarTabelaEnviado();
            novo();
        } catch (Exception e) {
            TransactionManager.rollback();
            JOptionPane.showMessageDialog(null, "Cliente não pode ser gravado!" + e);
        }
    }

    private boolean validarCampos() {
        if (jTextFieldID.getText().isEmpty() && jTextFieldCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O cliente deve ser preenchido!");
            return false;
        }
        return true;
    }

