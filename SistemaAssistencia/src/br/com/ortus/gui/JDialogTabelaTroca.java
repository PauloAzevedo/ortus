    private void remover() {
        int opcao = JOptionPane.showConfirmDialog(rootPane,
                "Deseja mesmo remover?", "Confirmação",
                JOptionPane.YES_NO_OPTION);
        if (opcao != JOptionPane.YES_OPTION) {
            return;
        }
        if (!jTextFieldID.getText().isEmpty()) {
            TransactionManager.beginTransaction();
            try {
                TabelaTrocaDao tDao = new TabelaTrocaDao();
                TabelaTroca troca = tDao.obterPorId(Integer.parseInt(jTextFieldID.getText()));
                tDao.remover(troca);
                TransactionManager.commit();
                povoarTabelaEnviado();
                povoarTabelaPendente();
                novo();
                JOptionPane.showMessageDialog(null, "Troca removida com sucesso!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro! Não foi possivel excluir a troca!\n" + e);
                return;
            }
        } else {
            JOptionPane.showMessageDialog(null, "ID inválido! Selecione uma troca válido para excluir!");
            return;
        }
    }

