    private void preencherCamposComDadosDaLinhaEnviados(int linha) {
        if (linha != -1 || linha > trocasEnviadas.size()) {
            //Collections.reverse(trocasEnviadas);
            TabelaTroca troca = trocasEnviadas.get(linha);
            preencherFormularioComDados(troca);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para editar a troca.");
            return;
        }
    }

    private String pegarDataAtual(String dataOrigem) {
        if (dataOrigem.equals("  /  /    ")) {
            String data = "";
            data = new Utilidades().calendarToString(Calendar.getInstance());
            return data;
        } else {
            return dataOrigem;
        }
    }

    private String pegarIPDaMaquina() {
        try {
            return InetAddress.getLocalHost().getHostAddress() + " - " + InetAddress.getLocalHost().getHostName();
        } catch (Exception e) {
            System.out.println(e);
            return " . . . ";
        }
    }
