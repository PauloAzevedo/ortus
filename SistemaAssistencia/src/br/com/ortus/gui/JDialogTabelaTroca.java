    private void povoarTabelaPendente() {
        TabelaTrocaDao cDao = new TabelaTrocaDao();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Cliente", "QTD", "Equipamento", "Data Pedido", "Data Max. Envio", "Obs", "Devolução", "Status Prod.", "Data Prod."});
        trocasPendentes = cDao.obterTrocas("Pendente");

        if (trocasPendentes != null) {
            for (TabelaTroca troca : trocasPendentes) {
                model.addRow(new Object[]{troca.getCliente(), troca.getQtd(), troca.getEquipamento(), troca.getDataPedido(), troca.getDataMaxEnvio(), troca.getObs(), troca.getDevolucao(), troca.getStatusProducao(), troca.getDataProducao()});
            }
            jTablePendentes.setModel(model);
            definirTamanhoDasColunas(jTablePendentes);
        }
        jTextFieldResultado.setText(String.valueOf(trocasPendentes.size()));
    }

