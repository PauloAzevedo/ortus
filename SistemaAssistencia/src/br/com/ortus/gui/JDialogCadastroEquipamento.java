    private void salvar() {

        if (validarCampos()) {
            int opcao = JOptionPane.showConfirmDialog(rootPane,
                    "Deseja mesmo salvar?", "Confirmação",
                    JOptionPane.YES_NO_OPTION);
            if (opcao != JOptionPane.YES_OPTION) {
                return;
            }
            Equipamento equi = new Equipamento();
            if (!jTextFieldID.getText().isEmpty()) {
                equi.setId(Integer.parseInt(jTextFieldID.getText()));
            }
            equi.setMoldeComponentes(listaMoldeComponente);

            equi.setNome(jComboBoxEquipamento.getSelectedItem().toString());
            equi.setNumeroSerie(jTextFieldNumeroSerie.getText());
            equi.setModelo(jTextFieldModelo.getText());
            equi.setProblemaConstatado(jTextAreaProblema.getText());
            equi.setServicoETestes(jTextAreaServicos.getText());

            equi.setAcessorios(buscarAcessorios());
            equi.setGarantia(verificarGarantia());

            Usuario user = (Usuario) HashSistema.hash.get("usuarioLogado");
            equi.setUsuario(user);

            TransactionManager.beginTransaction();
            try {
                EquipamentoDao eDao = new EquipamentoDao();
                eDao.persistir(equi);
                TransactionManager.commit();
                JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
                novo();
            } catch (Exception e) {
                TransactionManager.rollback();
                JOptionPane.showMessageDialog(null, "Equipamento não pode ser salvo!" + e);
            }


        }

    }

    private boolean validarCampos() {
        if (jTextFieldNumeroSerie.getText().isEmpty() || jTextFieldModelo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Contem o campo modelo e /ou número de série vazio(s)");
            return false;
        }
        return true;
    }

    private void buscarComponente() {
        JDialog jd = new JDialogCadastroMoldeComponente(null, true);
        jd.setLocationRelativeTo(null);
        jd.setVisible(true);
        MoldeComponente moldeComponente = (MoldeComponente) HashSistema.hash.get("moldeComponente");
        if (moldeComponente == null) {
            return;
        }
        HashSistema.hash.remove("moldeComponente");
        listaMoldeComponente.add(moldeComponente);

    }

    private void carregarTabelaComponentes() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{
                    "ID", "Componente", "U.M.", "Valor R$",  "Número Série"});


        if (listaMoldeComponente != null) {
            for (MoldeComponente comp : listaMoldeComponente) {
                model.addRow(new Object[]{comp.getComponente().getId(), comp.getComponente().getNome(), comp.getComponente().getUnidadeMedida(), comp.getValorFinalComponente(),  comp.getNumeroIdentificacao()});
            }
            jTableComponentes.setModel(model);
        }

    }

    private Object calcularDescontoICMS(double d, Double valor) {
        Double resultado = new Utilidades().calcularDescontoICMS(d, valor);
        return new Utilidades().formatarValorDoubleParaString(resultado);
    }

    private void removerComponenteLista() {
        int linha = jTableComponentes.getSelectedRow();
        if (linha != -1) {   
            if (linha < listaMoldeComponente.size()) {
                //equip.getComponentes().remove(linha);
                listaMoldeComponente.remove(linha);
            }
        }
    }

    private void pesquisar() {
        JDialog jd = new JDialogPesquisaEquipamento(null, true);
        jd.setLocationRelativeTo(null);
        jd.setVisible(true);
        Equipamento equi = (Equipamento) HashSistema.hash.get("equipamento");
        if (equi == null) {
            return;
        }
        HashSistema.hash.remove("equipamento");

        if (!equi.getMoldeComponentes().isEmpty()) {
            listaMoldeComponente = buscarComponentes(equi);
        }
        carregarTabelaComponentes();
        preencherFormularioComDados(equi);
        TransactionManager.commit();
    }

    private void preencherFormularioComDados(Equipamento equi) {
        jTextFieldID.setText(String.valueOf(equi.getId()));
        jTextFieldNumeroSerie.setText(equi.getNumeroSerie());
        jTextFieldModelo.setText(equi.getModelo());
        jComboBoxEquipamento.setSelectedIndex(buscarNomeEquip(equi));
        preencherJComboBoxAcessorios(equi);
        setarGarantia(equi);
        jTextAreaProblema.setText(equi.getProblemaConstatado());
        jTextAreaServicos.setText(equi.getServicoETestes());
    }

    private int buscarNomeEquip(Equipamento equi) {
        if (equi.getNome().equals("Ortusonic")) {
            return 0;
        } else if (equi.getNome().equals("Bioscaler")) {
            return 1;
        } else if (equi.getNome().equals("Sony I")) {
            return 2;
        } else if (equi.getNome().equals("Sony II")) {
            return 3;
        } else if (equi.getNome().equals("Kit Ortusonic")) {
            return 4;
        } else if (equi.getNome().equals("Kit Bio-jato")) {
            return 5;
        } else if (equi.getNome().equals("Bio-jato")) {
            return 6;
        }
        return -1;
    }

    private void novo() {
        jTextFieldID.setText(null);
        jTextFieldModelo.setText(null);
        jComboBoxEquipamento.setSelectedIndex(0);
        jTextFieldNumeroSerie.setText(null);
        if (listaMoldeComponente != null) {
            listaMoldeComponente.removeAll(listaMoldeComponente);
        }
        carregarTabelaComponentes();
        limparJCheckBoxAcessorios();
    }

    private void remover() {
        if (jTextFieldID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione um equipamento para excluir!");
            return;
        } else {
            int opcao = JOptionPane.showConfirmDialog(rootPane,
                    "Deseja mesmo excluir?", "Confirmação",
                    JOptionPane.YES_NO_OPTION);
            if (opcao != JOptionPane.YES_OPTION) {
                return;
            }
            TransactionManager.beginTransaction();
            try {
                EquipamentoDao eDao = new EquipamentoDao();
                Equipamento equi = eDao.obterPorId(Integer.parseInt(jTextFieldID.getText()));
                eDao.remover(equi);
                TransactionManager.commit();
                novo();
                JOptionPane.showMessageDialog(null, "Equipamento foi excluido com sucesso!");
            } catch (Exception e) {
                TransactionManager.rollback();
                JOptionPane.showMessageDialog(null, "Erro na exclusão" + e);
            }
        }
    }

    private String[] buscarAcessorios() {
        JCheckBox[] jc = {jCheckBox1, jCheckBox2, jCheckBox3, jCheckBox4, jCheckBox5, jCheckBox6, jCheckBox7, jCheckBox8, jCheckBox9, jCheckBox10, jCheckBox11, jCheckBox12, jCheckBox13, jCheckBox14,jCheckBox15};
        String[] acessorios = new String[15];
        for (int i = 0; i < jc.length; i++) {
            if (jc[i].isSelected()) {
                acessorios[i] = "1";
            } else {
                acessorios[i] = "0";
            }

        }
        return acessorios;
    }

    private void preencherJComboBoxAcessorios(Equipamento equi) {
        JCheckBox[] jc = {jCheckBox1, jCheckBox2, jCheckBox3, jCheckBox4, jCheckBox5, jCheckBox6, jCheckBox7, jCheckBox8, jCheckBox9, jCheckBox10, jCheckBox11, jCheckBox12, jCheckBox13, jCheckBox14, jCheckBox15};
        String[] acessorios = equi.getAcessorios();
        for (int i = 0; i < equi.getAcessorios().length; i++) {
            if (acessorios[i].equals("1")) {
                jc[i].setSelected(true);
            } else {
                jc[i].setSelected(false);
            }

        }
    }

    private void limparJCheckBoxAcessorios() {
        JCheckBox[] jc = {jCheckBox1, jCheckBox2, jCheckBox3, jCheckBox4, jCheckBox5, jCheckBox6, jCheckBox7, jCheckBox8, jCheckBox9, jCheckBox10, jCheckBox11, jCheckBox12, jCheckBox13, jCheckBox14,jCheckBox15};

        for (int i = 0; i < jc.length; i++) {
            jc[i].setSelected(false);
        }
    }

    private List<MoldeComponente> buscarComponentes(Equipamento equi) {
        List<MoldeComponente> listaAux = new ArrayList<MoldeComponente>();
        for (int i = 0; i < equi.getMoldeComponentes().size(); i++) {
            listaAux.add(equi.getMoldeComponentes().get(i));
        }
        return listaAux;
    }

    private Boolean verificarGarantia() {
        if(jRadioButtonSim.isSelected()){
            return true;
        }else{
            return false;
        }
    }

    private void setarGarantia(Equipamento equi) {
        if(equi.getGarantia() == true){
            jRadioButtonSim.setSelected(true);
        }else{
            jRadioButtonNao.setSelected(true);
        }
    }
