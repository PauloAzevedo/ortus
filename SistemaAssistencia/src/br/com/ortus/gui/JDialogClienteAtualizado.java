    private void remover() {
        int opcao = JOptionPane.showConfirmDialog(rootPane,
                "Deseja mesmo remover?", "Confirma��o",
                JOptionPane.YES_NO_OPTION);
        if (opcao != JOptionPane.YES_OPTION) {
            return;
        }
        if(!jTextFieldID.getText().isEmpty()){
            TransactionManager.beginTransaction();
            try{
                EnderecoDao eDao = new EnderecoDao();
                Endereco end = eDao.obterPorId(Integer.parseInt(jTextFieldIDEndereco.getText()));
                eDao.remover(end);
                ClienteDao cDao = new ClienteDao();
                Cliente cliente = cDao.obterPorId(Integer.parseInt(jTextFieldID.getText()));
                cDao.remover(cliente);
                TransactionManager.commit();
                JOptionPane.showMessageDialog(null, "Cliente removido com sucesso!");
                novo();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Erro! N�o foi possivel excluir o cliente!" + e);
                return;
            }
        }else{
            JOptionPane.showMessageDialog(null, "ID inv�lido! Selecione um cliente v�lido para excluir!");
            return;
        }
    }

    private void pesquisar() {
        JDialog jd = new JDialogPesquisaCliente(null, true);
        jd.setLocationRelativeTo(null);
        jd.setVisible(true);
        Cliente cliente = (Cliente) HashSistema.hash.get("cliente");
        if(cliente == null){
            return;
        }
        HashSistema.hash.remove("cliente");        
        preencherFormularioComOCliente(cliente);
    }

    private void preencherFormularioComOCliente(Cliente cliente) {
        jTextFieldID.setText(String.valueOf(cliente.getId()));
        jTextFieldNome.setText(cliente.getNome());
        jFormattedTextFieldCPF.setText(cliente.getCpf());
        jFormattedTextFieldCNPJ.setText(cliente.getCnpj());
        jFormattedTextFieldFone1.setText(cliente.getTelefone1());
        jFormattedTextFieldFone2.setText(cliente.getTelefone2());
        jTextFieldEmail.setText(cliente.getEmail());
        
        EnderecoDao eDao = new EnderecoDao();
        Endereco end = eDao.obterLista("cliente_id='"+cliente.getId()+"'").get(0);
        jTextFieldIDEndereco.setText(String.valueOf(end.getId()));
        jComboBoxTipo.setSelectedIndex(pegarTipoLogradouro(end));
        jTextFieldLogradouro.setText(end.getLogradouro());
        jTextFieldNumero.setText(end.getNumero());
        jTextFieldBairro.setText(end.getBairro());
        jFormattedTextFieldCEP.setText(end.getCep());
        Cidade cid = buscarCidade(end);
        jComboBoxEstado.setSelectedIndex(cid.getEstado().getIdEstado() - 1);
        jComboBoxCidade.setSelectedIndex(buscarIndexCidade(cid));
        return;
    }

    private int pegarTipoLogradouro(Endereco end) {
        if(end.getTpLogradouro().equals("Rua")){
            return 0;
        }
        return 1;
    }

    private Cidade buscarCidade(Endereco end) {
        Cidade cid = new CidadeDao().obterPorId(end.getCidade().getIdCidade());
        return cid;
    }

    private int buscarIndexCidade(Cidade cid) {
        List<Cidade> cidades = new CidadeDao().obterLista("estado_idEstado='" + cid.getEstado().getIdEstado() + "'");

        for (int i = 0; i < cidades.size(); i++) {
            if (cid.getCidade().equals(cidades.get(i).getCidade())) {
                return i;
            }
        }
        return 0;
    }
    
    private void povoarTabelaClientes() {
        ClienteDao cDao = new ClienteDao();
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{
                    "ID", "Nome","CPF", "CNPJ", "Telefone 1", "Telefone 2", "Cidade"});
        clientes = cDao.obterLista(filtro);
        
        if (clientes != null) {
            for (Cliente cliente : clientes) {
                model.addRow(new Object[]{cliente.getId(), cliente.getNome(), cliente.getCpf(), cliente.getCnpj(), cliente.getTelefone1(), cliente.getTelefone2(), pegarCidade(cliente)});
            }
            jTableClientes.setModel(model);            
        }
        jTextFieldResultado.setText(String.valueOf(clientes.size()));
    }

    private Object pegarCidade(Cliente c) {
        EnderecoDao eDao = new EnderecoDao();
        Endereco end = eDao.obterLista("cliente_id='" + c.getId()+"'").get(0);
        return end.getCidade();
    }

    private void alterar() {
        int linha = jTableClientes.getSelectedRow();
        if(linha != -1){
            Cliente c = clientes.get(linha);
            preencherFormularioComOCliente(c);
            //jPanelCadastro.setVisible(true);
            jTabbedPaneCliente.setSelectedIndex(0);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha para editar o componente.");
            return;
        }
    }

    private void filtrar() {
        if(jTextFieldFiltro.getText().isEmpty()){
            filtro = "";
            povoarTabelaClientes();
        }else{
            if(jComboBoxFiltro.getSelectedIndex() == 0){
                pesquisarPorID();
                filtro = "";
            }else if(jComboBoxFiltro.getSelectedIndex() == 1){
                pesquisarPorNome();
                filtro = "";
            }else if(jComboBoxFiltro.getSelectedIndex() == 2){
                pesquisarPorTelefone();
                filtro = "";
            }else if (jComboBoxFiltro.getSelectedIndex() == 3){
                pesquisarPorEmail();
                filtro = "";
            }else{
//                pesquisarPorValorMenorIgual();
                filtro = "";
            }
        }
    }

    private void pesquisarPorID() {
        if(verificarSeEntradaENumero()){
            filtro = "id='"+jTextFieldFiltro.getText()+"'";
            povoarTabelaClientes();
        }else{
            return;
        }
    }

    private boolean verificarSeEntradaENumero() {
        return new Utilidades().verificarSeEntradaENumeroInteger(jTextFieldFiltro.getText());
    }

    private void pesquisarPorNome() {
        filtro = "nome LIKE '%"+jTextFieldFiltro.getText()+ "%'";
        povoarTabelaClientes();
    }

    private void pesquisarPorTelefone() {
        filtro = "telefone1 LIKE '%"+jTextFieldFiltro.getText()+ "%' OR telefone2 LIKE '%"+jTextFieldFiltro.getText()+ "%'";
        povoarTabelaClientes();
    }

    private void pesquisarPorEmail() {
        filtro = "email LIKE '%"+jTextFieldFiltro.getText()+ "%'";
        povoarTabelaClientes();
    }
