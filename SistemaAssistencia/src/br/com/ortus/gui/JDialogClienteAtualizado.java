    private void salvar() {

        if (!validarCampos()) {
            return;
        }
        int opcao = JOptionPane.showConfirmDialog(rootPane,
                "Deseja salvar?", "Confirma��o",
                JOptionPane.YES_NO_OPTION);
        if (opcao != JOptionPane.YES_OPTION) {
            return;
        }
        Cliente cliente = new Cliente();
        if (!jTextFieldID.getText().isEmpty()) {
            cliente.setId(Integer.parseInt(jTextFieldID.getText()));
        }
        cliente.setNome(jTextFieldNome.getText().toUpperCase());
        cliente.setCpf(jFormattedTextFieldCPF.getText().toUpperCase());
        cliente.setCnpj(jFormattedTextFieldCNPJ.getText().toUpperCase());
        cliente.setTelefone1(jFormattedTextFieldFone1.getText().toUpperCase());
        cliente.setTelefone2(jFormattedTextFieldFone2.getText().toUpperCase());
        cliente.setEmail(jTextFieldEmail.getText().toUpperCase());

        Endereco end = new Endereco();
        if (!jTextFieldID.getText().isEmpty()) {
            end.setId(Integer.parseInt(jTextFieldID.getText()));
        }
        end.setTpLogradouro(jComboBoxTipo.getSelectedItem().toString());
        end.setLogradouro(jTextFieldLogradouro.getText().toUpperCase());
        end.setNumero(jTextFieldNumero.getText().toUpperCase());
        end.setBairro(jTextFieldBairro.getText().toUpperCase());
        end.setCep(jFormattedTextFieldCEP.getText());
        end.setCidade(setarCidade());
        end.setCliente(cliente);


        TransactionManager.beginTransaction();
        try {
            ClienteDao cDao = new ClienteDao();
            EnderecoDao eDao = new EnderecoDao();

            cDao.persistir(cliente);
            eDao.persistir(end);
            TransactionManager.commit();

            JOptionPane.showMessageDialog(null, "Cliente gravado com sucesso!");
            povoarTabelaClientes();
            novo();
        } catch (Exception e) {
            TransactionManager.rollback();
            JOptionPane.showMessageDialog(null, "Cliente n�o pode ser gravado!" + e);
        }
    }

